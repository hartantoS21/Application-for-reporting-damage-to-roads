        tabelTampil.addColumn("Alamat");
        tabelTampil.addColumn("No_Telepon");
        tabelTampil.addColumn("Email");
        tabelTampil.addColumn("Password");
        try{
            String url;
            Class.forName("com.mysql.jdbc.Driver");
            url="jdbc:mysql://localhost:3306/aplikasi_phtrs?zeroDateTimeBehavior=convertToNull";
            connect=DriverManager.getConnection(url,"root","");
            sql2="SELECT * FROM register order by nobp asc";
            statemen=connect.createStatement();
            ResultSet rs= statemen.executeQuery(sql2);
            while(rs.next()){
                tabelTampil.addRow(new Object[]{
                rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5),
                rs.getString(6),
                rs.getString(7),
            });
            }
            TableIdentitasPelapor.setModel(tabelTampil);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ada kesalahan");
        }
