    private void ButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEditActionPerformed
        String url;
        try{
            DefaultTableModel model =(DefaultTableModel)TableIdentitasPelapor.getModel();
            Class.forName("com.mysql.jdbc.Driver");
            url="jdbc:mysql://localhost:3306/karyawan?zeroDateTimeBehavior=convertToNull";
            connect=DriverManager.getConnection(url,"root","");
            sql1 = "UPDATE INTO register VALUES('"+TextFieldUserID.getText()+"',"
                                           + "'"+TextFieldNamaLengkap.getText()+"',"
                                           + "'"+TextFieldNoIdentitas.getText()+"',"
                                           + "'"+TextAreaAlamat.getText()+"',"
                                           + "'"+TextFieldNoTelepon.getText()+"',"
                                           + "'"+TextFieldEmail.getText()+"',"
                                           + "'"+TextFieldPassword.getText()+"')";
            if(TableIdentitasPelapor.getSelectedRow() == -1){
                if(TableIdentitasPelapor.getRowCount() == 0){
                    JOptionPane.showMessageDialog(null,"Tabel masih kosong");
                }else{
                    JOptionPane.showMessageDialog(null,"Klik Tabel");
                }
            }else{
                statemen=connect.createStatement();
                statemen.execute(sql1);
                model.setValueAt(TextFieldUserID.getText(), TableIdentitasPelapor.getSelectedRow(), 0);
                model.setValueAt(TextFieldNamaLengkap.getText(), TableIdentitasPelapor.getSelectedRow(), 1);
                model.setValueAt(TextFieldNoIdentitas.getText(), TableIdentitasPelapor.getSelectedRow(), 2);
                model.setValueAt(TextAreaAlamat.getText(), TableIdentitasPelapor.getSelectedRow(), 3);
                model.setValueAt(TextFieldNoTelepon.getText(), TableIdentitasPelapor.getSelectedRow(), 4);
                model.setValueAt(TextFieldEmail.getText(), TableIdentitasPelapor.getSelectedRow(), 5);
                model.setValueAt(TextFieldPassword.getText(), TableIdentitasPelapor.getSelectedRow(), 6);
                JOptionPane.showMessageDialog(null,"Identitas Anda sudah terdaftar didatabase");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Gagal, Pastiksn ID yang Anda Masukkan Belum Terdaftar dan Pastikan Database sudah terhubung.");
        }
    }//GEN-LAST:event_ButtonEditActionPerformed

